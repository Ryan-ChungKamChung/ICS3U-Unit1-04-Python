# Program finding area and perimeter of a rectangle


def main():
    print("With the dimensions of 3cm and 5cm, the area would be")
    print("3*5cm^2")
    print("={}".format(3*5) + "cm^2")
    print("With the dimensions of 3cm and 5cm, the perimeter would be")
    print("2(5+3)cm")
    print("={}".format(2*(5+3)) + "cm")


if __name__ == "__main__":
    main()
